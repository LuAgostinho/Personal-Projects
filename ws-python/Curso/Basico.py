#Tipos padrões de variáveis
Numero_inteiro: int
Numero_com_virgula: float
Caracteres: str

#Comando para printar mensagens no console
print("Ola mundo")

Caracteres = "Teste de print"
Numero_inteiro = 2
Numero_com_virgula = 3.1456

#Print de variaveis
print(f"Teste = {Caracteres} \n")
print(f"Numero_inteiro = {Numero_inteiro}\n")
#Print de variável com limite de casas decimais
print(f"Numero_com_virgula = {Numero_com_virgula:.2f}")